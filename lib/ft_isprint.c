#include <stdio.h>

int ft_isprint (char c)
{
	if (c >= ' ' && c <= '~')
	{
		return (0);
	}
	return (1);
}
int main ()
{
	char c;
	c = 'c';
	printf("%d",ft_isprint(c));
}
