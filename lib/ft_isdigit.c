#include <stdio.h>

int ft_isdigit (char c)
{
	if (c >= '0' && c <= '9')
	{
		return (0);
	}
	return (1);
}
int main ()
{
	char c;
	c = 'c';
	printf("%d",ft_isdigit(c));
}
