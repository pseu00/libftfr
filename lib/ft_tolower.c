#include <stdio.h>

int ft_tolower(int c)
{
	unsigned char a;
	a = c;
	
	if (a >= 'A' && a <= 'B')
	{
		a += 32;
	}
	return (a);
}
int main ()
{
	char c;
	c = 'A';
	printf("%c", ft_tolower(c));
}
