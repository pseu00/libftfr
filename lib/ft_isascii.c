#include <stdio.h>

int ft_isascii (char c)
{
	if (c >= 0 && c <= 127)
	{
		return (0);
	}
	return (1);
}
int main ()
{
	char c;
	c = 'c';
	printf("%d",ft_isascii(c));
}
