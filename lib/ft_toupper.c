#include <stdio.h>

int ft_toupper(int c)
{
	unsigned char a;
	a = c;
	
	if (a >= 'a' && a <= 'z')
	{
		a -= 32;
	}
			return (a);
}
int main()
{
	char c;
	c = 'a';
	printf("%c", ft_toupper(c));
}

